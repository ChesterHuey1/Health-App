export type Category = 'knee' | 'hip' | 'shoulder' | 'spine' | 'ankle' | 'acl' | 'full_body'
export type Difficulty = 1 | 2 | 3 | 4 | 5

export interface PoseRule {
  // landmark indices: [A, vertex, C] — angle is measured at vertex
  landmarks: [number, number, number]
  label: string
  minAngle: number
  maxAngle: number
  // angle value that counts as completing one rep cycle
  repTriggerAngle: number
  repDirection: 'above' | 'below' // rep fires when angle goes below/above threshold
  feedbackLow: string
  feedbackHigh: string
  feedbackCorrect: string
}

